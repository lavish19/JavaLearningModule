import java.util.Scanner;
//program to demonstrate length of string variable using length function
public class Length {
	public static void main(String[] args) {
	String l;
	Scanner s=new Scanner(System.in);
	System.out.println("enter the value of the variable");
	l=s.nextLine();
	System.out.println("value entered is "+l);
	int length=l.length();
	System.out.println("length of the variable is "+length);
}}
	
	
	
	
	


